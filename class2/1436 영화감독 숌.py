# 16660 -> 16661 -> 16662 -> 16663 -> ...... -> 16669
# -> 17666 -> 18666 -> 19666 -> 20666 -> 21666 -> 22666 -> 23666 -> 24666 -> 25666
# -> 26660 -> 2

# 10 ^ 3 * 0 + 666 -> 10 ^ 3 * 1 + 666 -> 10 ^ 3 * 2 + 666 -> 10 ^ 3 * 3 + 666  
# 일반화해서 생각하면, N번째 영화의 제목은 세상의 종말 (N번째로 작은 종말의 수) 와 같다.
    
# 0 ~ 5 -> 6는 0 ~ 9 -> 7 ~ 15 -> 16는 0 ~ 9 -> 17 ~ 25 -> 26는 0 ~ 9
#  
# 1
# 7(6 + 1)번째  ~ 16(6 + 10)번째    
# 26(6 + 20)번째 ~ 35번째(6 + 29)번째
# 45(6+ 39) 번째 ~ 54번째(6 + 48)번쨰

def find_n_th_number(n):
    count = 0
    number = 666
    while True:
        if "666" in str(number):  # "666"이 포함된 숫자
            count += 1
            if count == n:  # n번째 숫자를 찾으면 반환
                return number
        number += 1  # 다음 숫자로 진행

# 예시: 10번째 "666"이 포함된 숫자 찾기
n = int(input())
print(find_n_th_number(n))  
